_PUNCT_END = [".", ".", ".", "!", "?", ":", ";", ""]
_ALPHABET_LINES = [
    "אבגדהוזחטיכךלמםנןסעפףצץקרשת",
    "תשרקץצףפעסןנםמלךכיטחזוהדגבא",
]


def make_lines(count: int, seed: int = 7, min_words: int = 4, max_words: int = 9) -> List[str]:
    """יוצר ``count`` שורות אימון אקראיות אך דטרמיניסטיות (seed קבוע)."""
    rng = random.Random(seed)
    lines: List[str] = []
    lines.extend(_ALPHABET_LINES)
    while len(lines) < count:
        n = rng.randint(min_words, max_words)
        words = [rng.choice(_WORDS) for _ in range(n)]
        # מדי פעם משלבים מספרים ופיסוק פנימי - מופיעים בעלונים וספרים
        if rng.random() < 0.30:
            pos = rng.randrange(len(words))
            words.insert(pos, str(rng.choice([rng.randint(1, 31), rng.randint(1900, 2030), rng.randint(1, 999)])))
        if rng.random() < 0.15:
            pos = rng.randrange(1, len(words))
            words[pos - 1] = words[pos - 1] + ","
        if rng.random() < 0.12:
            words = ['"' + words[0]] + words[1:]
            words[-1] = words[-1] + '"'
        line = " ".join(words) + rng.choice(_PUNCT_END)
        lines.append(line)
    return lines[:count]
